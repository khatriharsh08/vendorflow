function resolveIconName(name) {
    if (typeof name !== 'string') {
        return null;
    }

    const normalized = name.trim().toLowerCase();

    return ICON_ALIASES[normalized] || normalized;
}

export function hasAppIcon(name) {
    const key = resolveIconName(name);

    return Boolean(key && ICON_PATHS[key]);
}

export default function AppIcon({
    name,
    className = 'h-5 w-5',
    strokeWidth = 1.8,
    fallback = null,
    title = null,
}) {
    const key = resolveIconName(name);

    if (!key || !ICON_PATHS[key]) {
        return fallback;
    }

    return (
        <svg
            className={className}
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
            aria-hidden={title ? undefined : true}
            role={title ? 'img' : undefined}
        >
            {title && <title>{title}</title>}
            <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={strokeWidth}
                d={ICON_PATHS[key]}
            />
        </svg>
    );
}
